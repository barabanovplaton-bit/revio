/** Рендер SVG иконки по индексу */
export function ProjectIcon({
  index,
  color,
  className = "h-6 w-6",
}: {
  index: number;
  color: string;
  className?: string;
}) {
  const icon = PROJECT_ICONS[index] || PROJECT_ICONS[PROJECT_ICONS.length - 1];
  return (
    <div className={className} style={{ color }}>
      {icon.svg}
    </div>
  );
}

/** Получить индекс по label (для обратной совместимости) */
export function getIconIndex(label: string): number {
  const idx = PROJECT_ICONS.findIndex((i) => i.label === label);
  return idx >= 0 ? idx : PROJECT_ICONS.length - 1;
}
